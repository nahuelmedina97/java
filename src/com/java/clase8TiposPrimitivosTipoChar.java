package com.java;

public class clase8TiposPrimitivosTipoChar {
    public static void main(String[] args) {
     // TIPOS PRIMITIVOS TIPO CHAR 

        char miCaracter = 'a' ; 
       System.out.println("miCaracter= " + miCaracter); 
       
       char VarChar = '\u0021'; 
       System.out.println("VarChar = " + VarChar);
       
       char VarCharDecimal = 33; 
       System.out.print("VarCharDecimal= " + VarCharDecimal); 
       
       char VarCharSimbolo = '!'; 
       System.out.println("VarCharSimbolo = " + VarCharSimbolo);  
       
       int variableEnteraSimbolo = '!'; 
       System.out.print("variableEnteraSimbolo = " + variableEnteraSimbolo); 
       
       int letra = 'A'; 
       System.out.print("letra = " + letra); 
    }
    
}
